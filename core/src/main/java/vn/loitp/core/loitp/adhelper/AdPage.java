public class AdPage {
    private String title;
    private String msg;
    private String urlAd;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getUrlAd() {
        return urlAd;
    }

    public void setUrlAd(String urlAd) {
        this.urlAd = urlAd;
    }
}
